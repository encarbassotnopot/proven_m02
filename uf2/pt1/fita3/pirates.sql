CREATE TABLE PIRATES_SENSOR (
	DATA_HORA DATE
		CONSTRAINT SENSOR_DH_PK PRIMARY KEY,
	FITXER VARCHAR2(30)
);


CREATE TABLE PIRATES_CLAUS (
	CLAU_ID VARCHAR2(16)
		CONSTRAINT CLAU_ID_PK PRIMARY KEY
);

CREATE TABLE PIRATES_EMPLEATS (
	EMPLEAT_ID NUMBER(6)
		CONSTRAINT EMPLEAT_ID_PK PRIMARY KEY,
	DNI VARCHAR2(9)
		CONSTRAINT EMPLEAT_DNI_UK UNIQUE,
	NOM VARCHAR2(12)
		CONSTRAINT EMPLEAT_NOM_NN NOT NULL,
	COGNOMS VARCHAR2(30)
		CONSTRAINT EMPLEAT_COGNOM_NN NOT NULL,
	TELEFON VARCHAR2(16)
		CONSTRAINT EMPLEAT_TELF_UK UNIQUE,
	EMAIL VARCHAR2(50)
		CONSTRAINT EMPLEAT_MAIL_UK UNIQUE,
	CONSTRAINT EMPLEATS_DNI_CK CHECK (REGEXP_LIKE(DNI, '^[0-9]{8}[A-Z]$')),
	CONSTRAINT EMPLEATS_MAIL_CK CHECK (REGEXP_LIKE(EMAIL, '^.+@.+$'))
);

CREATE TABLE PIRATES_VEHICLES (
	MATRICULA VARCHAR2(7)
		CONSTRAINT COTXE_MATRICULA_PK PRIMARY KEY,
	ANY_MODEL NUMBER(4),
	MARCA VARCHAR2(16),
	COLOR VARCHAR2(16),
	CONSTRAINT VEHICLES_MAT_CK CHECK (REGEXP_LIKE(MATRICULA, '^[0-9]{4}[A-Z]{3}$'))
);

CREATE TABLE PIRATES_TIQUETS (
	ID_TIQUET NUMBER(6)
		CONSTRAINT TIQUET_ID_PK PRIMARY KEY,
	DATA_CREACIO DATE
		CONSTRAINT TIQUET_DATA_NN NOT NULL,
	MISSATGE_CREACIO VARCHAR2(1000),
	DATA_RESOLUCIO DATE,
	MISSATGE_RESOLUCIO VARCHAR2(1000),
	EMPLEAT_ID NUMBER(6)
		CONSTRAINT TIQUETS_EMP_ID_FK REFERENCES PIRATES_EMPLEATS(EMPLEAT_ID) ON DELETE CASCADE
);

CREATE TABLE PIRATES_POSEEIX (
	DATA DATE,
	CLAU_ID VARCHAR2(16)
		CONSTRAINT POSEEIX_CLAU_FK REFERENCES PIRATES_CLAUS(CLAU_ID) ON DELETE CASCADE,
	EMPLEAT_ID NUMBER(6)
		CONSTRAINT POSEEIX_EMP_ID_FK REFERENCES PIRATES_EMPLEATS(EMPLEAT_ID) ON DELETE CASCADE,
	FI_VALIDESA DATE,
	CONSTRAINT POSEEIX_DH_CLAU_PK PRIMARY KEY (DATA, CLAU_ID)
);

CREATE TABLE PIRATES_CONDUEIX (
	DATA_HORA DATE,
	MATRICULA VARCHAR2(7)
		CONSTRAINT CONDUEIX_MAT_FK REFERENCES PIRATES_VEHICLES(MATRICULA) ON DELETE CASCADE,
	EMPLEAT_ID NUMBER(6)
		CONSTRAINT CONDUEIX_EMP_ID_FK REFERENCES PIRATES_EMPLEATS(EMPLEAT_ID) ON DELETE CASCADE,
	CONSTRAINT CONDUEIX_DH_MAT_PK PRIMARY KEY (DATA_HORA, MATRICULA)

);

CREATE TABLE PIRATES_ACCESSOS (
	DATA_HORA DATE,
	CLAU_ID VARCHAR2(16)
		CONSTRAINT ACCES_CLAU_FK REFERENCES PIRATES_CLAUS(CLAU_ID) ON DELETE CASCADE,
	CONSTRAINT ACCESSOS_DH_CLAU_PK PRIMARY KEY (DATA_HORA, CLAU_ID)
);

